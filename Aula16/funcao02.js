function soma(n1, n2) {
    return n1 + n2
}

function subtracao(n1, n2) {
    return n1 - n2
}

function multiplicacao(n1, n2) {
    return n1 * n2
}

function divisao(n1, n2) {
    return n1 / n2
}

console.log(soma(2, 5))
console.log(subtracao(5, 3))
console.log(multiplicacao(5, 5))
console.log(divisao(80, 4))